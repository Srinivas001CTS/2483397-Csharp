
using System;

public class HelloWorld
{
    public static void Main(string[] args)
    {
        //Post Increment
        int a=5;
        int b=a++; //use the value first, then increments
        Console.WriteLine(b);
        //Pre Increment
        int x=5;
        int y=++x;  //Increments first then use the value
        Console.WriteLine(y);
        
        
        
        
    }
}
